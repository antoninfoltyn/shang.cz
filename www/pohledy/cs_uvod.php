

<h2>MusicOlomouc</h2>

<div class="container-600">

<p class="italic">"...zvukoChmýř...svitoZvuk...zkapalnění i krápnění.... tón jako květenství, mračno, dým..."</p>

<p>MusicOlomouc - mezinárodní festival soudobé světové a české hudby a hudby mezních žánrů ... místo setkání skladatelů, interpretů a zanícených fanoušků netradičních zvukových vesmírů ... neobvyklá nástrojová uskupení, nové zvukové možnosti, využití prostoru, světla, tmy ... od roku 2009 přináší vždy v druhé polovině dubna do Olomouce české a světové premiéry nových kompozic i již osvědčená díla hudební avantgardy druhé poloviny 20. a 21. století v interpretaci předních českých i zahraničních umělců a ansámblů. MusicOlomouc je místem křehkých zjevování.</p>

